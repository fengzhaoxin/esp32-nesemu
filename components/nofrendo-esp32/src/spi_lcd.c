static void  lcd_init ()
{
    int cmd=0;
    const lcd_init_cmd_t* lcd_init_cmds;

    //Initialize non-SPI GPIOs
    gpio_config_t io_conf = {};
    io_conf.pin_bit_mask = ((1ULL<<PIN_NUM_DC) | (1ULL<<PIN_NUM_RST) | (1ULL<<PIN_NUM_BCKL));
    io_conf.mode = GPIO_MODE_OUTPUT;
    io_conf.pull_up_en = true;
    gpio_config(&io_conf);

    //Reset the display
    gpio_set_level(PIN_NUM_RST, 0);
    vTaskDelay(100 / portTICK_RATE_MS);
    gpio_set_level(PIN_NUM_RST, 1);
    vTaskDelay(100 / portTICK_RATE_MS);

    printf("LCD ILI9341 initialization.\n");
    lcd_init_cmds = ili_init_cmds;


    //Send all the commands
    while (lcd_init_cmds[cmd].databytes!=0xff) {
        lcd_cmd(spi, lcd_init_cmds[cmd].cmd);
        lcd_data(spi, lcd_init_cmds[cmd].data, lcd_init_cmds[cmd].databytes&0x1F);
        if (lcd_init_cmds[cmd].databytes&0x80) {
            vTaskDelay(100 / portTICK_RATE_MS);
        }
        cmd++;
    }

    ///Enable backlight
    gpio_set_level(PIN_NUM_BCKL, 1);
}

static void spi_master_init()
{
    esp_err_t ret;
    spi_bus_config_t buscfg={
        .miso_io_num=PIN_NUM_MISO,
        .mosi_io_num=PIN_NUM_MOSI,
        .sclk_io_num=PIN_NUM_CLK,
        .quadwp_io_num=-1,
        .quadhd_io_num=-1,
        .max_transfer_sz=PARALLEL_DATAS*320*2+8
    };
    spi_device_interface_config_t devcfg={
        .clock_speed_hz=SPI_MASTER_FREQ_40M,//10*1000*1000,           //Clock out at 10 MHz
        .mode=0,                                //SPI mode 0
        .spics_io_num=PIN_NUM_CS,               //CS pin
        .queue_size=7,                          //We want to be able to queue 7 transactions at a time
        .pre_cb=lcd_spi_pre_transfer_callback,  //Specify pre-transfer callback to handle D/C line
    };
    //Initialize the SPI bus
    ret=spi_bus_initialize(LCD_HOST, &buscfg, SPI_DMA_CH_AUTO);
    ESP_ERROR_CHECK(ret);
    //Attach the LCD to the SPI bus
    ret=spi_bus_add_device(LCD_HOST, &devcfg, &spi);
    ESP_ERROR_CHECK(ret);
}

extern uint16_t myPalette[];

void ili9341_write_frame(const uint16_t xs, const uint16_t ys, const uint16_t width, const uint16_t height, const uint8_t * data[]){
    int x, y, t_i;
    int i;
    uint16_t t1, t2, x1, y1;
    // uint16_t *temp = heap_caps_malloc(width*PARALLEL_DATAS*sizeof(uint16_t), MALLOC_CAP_DMA);
    uint16_t *temp = malloc(width*PARALLEL_DATAS*sizeof(uint16_t));
    memset(temp,0,width*PARALLEL_DATAS);
    int first_sending=1;

    for (y=0; y<height; y++) {
        x1 = xs+(width-1);
        y1 = ys+y+(height-1);
        //Calculate a line.
        x = 0;
        t_i=0;
        while (x<width) {
            for (i=0; i<PARALLEL_DATAS; i++) {
                if(data == NULL){
                    temp[t_i++] = 0;
                    temp[t_i++] = 0;
                    x += 2;
                    continue;
                }
                t1 = myPalette[(unsigned char)(data[y][x])]; x++;
                t2 = myPalette[(unsigned char)(data[y][x])]; x++;
                temp[t_i++] = t1;
                temp[t_i++] = t2;
            }
        }

        //Finish up the sending process of the previous line, if any
        if (first_sending!=1) send_line_finish();
        first_sending=0;
        //Send the line we currently calculated.
        send_lines(xs, x1, ys+y, y1, width, temp);
        //The line set is queued up for sending now; the actual sending happens in the
        //background. We can go on to calculate the next line set as long as we do not
        //touch line[sending_line]; the SPI sending process is still reading from that.
    }
    // heap_caps_free(temp);
    free(temp);
}

void ili9341_init()
{
    spi_master_init();
    lcd_init ();
}